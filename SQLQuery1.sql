-- DROP SP DANG NHAP
CREATE PROCEDURE DANGNHAP @EMAIL VARCHAR(50), @MATKHAU NVARCHAR(50)
AS 
BEGIN
	DECLARE @STATUS INT
	IF EXISTS(SELECT * FROM NHANVIEN 
	WHERE EMAIL = @EMAIL AND MATKHAU = @MATKHAU)
		SET @STATUS = 1
	ELSE 
		SET @STATUS = 0
	SELECT @STATUS
END
GO

