-- DROP PROC DOIMK
CREATE PROC DOIMK 
@EMAIL VARCHAR(50),
@ODDPASS VARCHAR(100),
@NEWPASS VARCHAR(100)
AS 
BEGIN
	DECLARE @OP VARCHAR(100)
	SELECT @OP = MATKHAU FROM NHANVIEN WHERE EMAIL = EMAIL
	IF @OP = @ODDPASS
	BEGIN 
		UPDATE NHANVIEN SET MATKHAU = @NEWPASS WHERE EMAIL = @EMAIL
		RETURN 1
	END
	ELSE
		RETURN -1
END
GO

